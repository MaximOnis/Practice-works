"""
3. Напишіть програму, яка запитує на введення число. Якщо воно позитивне, то на
екран виводиться цифра 1. Якщо число від'ємне, виводиться -1. Якщо введене
число - це 0, то на екран виводиться 0. Використовуйте в коді умовний оператор
множинного розгалуження.
"""
try:
    num = float(input("Number:"))
    if num > 0:
        print(1)
    elif num < 0:
        print(-1)
    else:
        print(0)
except ValueError:
    print("Wrong input.")