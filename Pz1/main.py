# варіант 17: Напишіть програму для завдання напряму руху робота
def setside():
    print("Оберіть один з 4 напрямів для робота(Прямо, Назад, Вправо, Вліво)")
    side = input("Напрям: ").lower()
    if side == "вперед" or side == "вправо" or side == "вліво" or side == "назад":
        print("Робот буде рухатись ", side, ".", sep="")
    else:
        print("Робот не зрозумів куди рухатись.")


setside()
